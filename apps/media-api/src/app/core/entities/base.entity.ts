import { BcBaseInterface } from '@core-lib';
import { ApiObjectId } from '@mediashare/shared';
import { ApiProperty } from '@nestjs/swagger';
import { ObjectId } from 'bson';
import { ObjectIdColumn, Entity, CreateDateColumn, UpdateDateColumn, Column } from 'typeorm';

export type DeepPartial<T> = {
  [P in keyof T]?: T[P] extends Array<infer U>
    ? Array<DeepPartial<U>>
    : T[P] extends ReadonlyArray<infer U>
    ? ReadonlyArray<DeepPartial<U>>
    : DeepPartial<T[P]> | T[P];
};

export abstract class BcBaseEntity<M> implements BcBaseInterface {
  @ObjectIdColumn()
  _id: ObjectId;

  constructor(model?: Partial<M>) {
    Object.assign(this, model);
  }
}

@Entity()
export class BcEntity implements BcBaseInterface {
  @ObjectIdColumn()
  @ApiObjectId()
  _id: Readonly<ObjectId>;

  @CreateDateColumn()
  @ApiProperty({ readOnly: true, type: 'date' })
  createdAt: Readonly<Date>;
  @UpdateDateColumn()
  updatedDate: Date;

  @Column()
  @ApiObjectId({ required: false, description: 'Created by type is generated by the @CreateDto decorator' })
  createdBy?: Readonly<ObjectId>;
}
